<script>
   function guardar(event){
     event.preventDefault();
     let data = new FormData();
     let form = $('#form').serializeArray();
     var band = true;
     form.forEach((item)=>{
         band = item.value != '';
         data.append(item.name,item.value);
     });
     if(band){
        $.ajax({
           type: 'POST',
           url: '{{route('landing.store')}}',
           data: data,
           contentType: false,
           processData: false,
           success: (msg)=>{
               if(msg.status == 'ok'){
                   Swal.fire({
                       text: msg.message
                   });
                  $('#nombre').val('');
                  $('#correo').val('');
               }else{
                   Swal.fire({
                       title:'Error',
                       text: msg.message
                   });
               }
           }
        });
     }else{
         Swal.fire({
             text: 'Debe rellenar todos los campos del formulario'
         });
     }
   }
